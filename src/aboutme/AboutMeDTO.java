package aboutme;

public class AboutMeDTO {
	private int aboutMeID;
	private String aboutMeTitle;
	private String userID;
	private String aboutMeDate;
	private String aboutMeContent;
	private int aboutMeHit;
	public int getAboutMeID() {
		return aboutMeID;
	}
	public void setAboutMeID(int aboutMeID) {
		this.aboutMeID = aboutMeID;
	}
	public String getAboutMeTitle() {
		return aboutMeTitle;
	}
	public void setAboutMeTitle(String aboutMeTitle) {
		this.aboutMeTitle = aboutMeTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getAboutMeDate() {
		return aboutMeDate;
	}
	public void setAboutMeDate(String aboutMeDate) {
		this.aboutMeDate = aboutMeDate;
	}
	public String getAboutMeContent() {
		return aboutMeContent;
	}
	public void setAboutMeContent(String aboutMeContent) {
		this.aboutMeContent = aboutMeContent;
	}
	public int getAboutMeHit() {
		return aboutMeHit;
	}
	public void setAboutMeHit(int aboutMeHit) {
		this.aboutMeHit = aboutMeHit;
	}
	
	
}
