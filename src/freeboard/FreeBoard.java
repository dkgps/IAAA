package freeboard;

public class FreeBoard {
	private int freeBoardID;
	private String freeBoardTitle;
	private String userID;
	private String freeBoardDate;
	private String freeBoardContent;
	private int freeBoardAvailable;
	public int getFreeBoardID() {
		return freeBoardID;
	}
	public void setFreeBoardID(int freeBoardID) {
		this.freeBoardID = freeBoardID;
	}
	public String getFreeBoardTitle() {
		return freeBoardTitle;
	}
	public void setFreeBoardTitle(String freeBoardTitle) {
		this.freeBoardTitle = freeBoardTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getFreeBoardDate() {
		return freeBoardDate;
	}
	public void setFreeBoardDate(String freeBoardDate) {
		this.freeBoardDate = freeBoardDate;
	}
	public String getFreeBoardContent() {
		return freeBoardContent;
	}
	public void setFreeBoardContent(String freeBoardContent) {
		this.freeBoardContent = freeBoardContent;
	}
	public int getFreeBoardAvailable() {
		return freeBoardAvailable;
	}
	public void setFreeBoardAvailable(int freeBoardAvailable) {
		this.freeBoardAvailable = freeBoardAvailable;
	}
	
	
	
}
