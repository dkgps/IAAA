package research;

public class ResearchDTO {
	private String userID;
	private int researchID;
	private String researchTitle;
	private String researchContent;
	private String researchDate;
	private int researchHit;
	private String researchFile;
	private String researchRealFile;
	private int researchGroup;
	private int researchSequence;
	private int researchLevel;
	private int researchAvailable;
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public int getResearchID() {
		return researchID;
	}
	public void setResearchID(int researchID) {
		this.researchID = researchID;
	}
	public String getResearchTitle() {
		return researchTitle;
	}
	public void setResearchTitle(String researchTitle) {
		this.researchTitle = researchTitle;
	}
	public String getResearchContent() {
		return researchContent;
	}
	public void setResearchContent(String researchContent) {
		this.researchContent = researchContent;
	}
	public String getResearchDate() {
		return researchDate;
	}
	public void setResearchDate(String researchDate) {
		this.researchDate = researchDate;
	}
	public int getResearchHit() {
		return researchHit;
	}
	public void setResearchHit(int researchHit) {
		this.researchHit = researchHit;
	}
	public String getResearchFile() {
		return researchFile;
	}
	public void setResearchFile(String researchFile) {
		this.researchFile = researchFile;
	}
	public String getResearchRealFile() {
		return researchRealFile;
	}
	public void setResearchRealFile(String researchRealFile) {
		this.researchRealFile = researchRealFile;
	}
	public int getResearchGroup() {
		return researchGroup;
	}
	public void setResearchGroup(int researchGroup) {
		this.researchGroup = researchGroup;
	}
	public int getResearchSequence() {
		return researchSequence;
	}
	public void setResearchSequence(int researchSequence) {
		this.researchSequence = researchSequence;
	}
	public int getResearchLevel() {
		return researchLevel;
	}
	public void setResearchLevel(int researchLevel) {
		this.researchLevel = researchLevel;
	}
	public int getResearchAvailable() {
		return researchAvailable;
	}
	public void setResearchAvailable(int researchAvailable) {
		this.researchAvailable = researchAvailable;
	}
	
	
	
}
